addEventHandler("onClientResourceStart", resourceRoot,
	function()
		for index, value in pairs(wearableList) do
			thread:foreach(value['list'], function(data)
				if fileExists("mods/"..data['txd']..".txd") then
					txd = engineLoadTXD("mods/"..data['txd']..".txd")
					engineImportTXD(txd, data['modelid'])
				end
				if fileExists("mods/"..data['dff']..".dff") then
					dff = engineLoadDFF("mods/"..data['dff']..".dff")
					engineReplaceModel(dff, data['modelid'])
				end
			end)
		end

		for index, value in pairs(wearableList) do
			thread:foreach(value['list'], function(data)
				removeWorldModel(data.modelid, 9999, 0, 0, 0)
			end)
		end
	end
)
