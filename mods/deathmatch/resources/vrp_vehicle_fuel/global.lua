function getMaxFuel(id)
	if isElement(id) then
		id = getElementModel(id)
	end
	
	if not tonumber(id) then 
		return 
	end

	local id = tonumber(id)

	if carFuel[id] then
		return carFuel[id][1]
	else
		return 100
	end
end